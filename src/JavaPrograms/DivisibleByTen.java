
package JavaPrograms;

public class DivisibleByTen {

	public static void main(String[] args) {
		
		//>>Java program: All numbers divisible by 10 from 1 to 1000 
		
		for (int i = 0; i < 1000; i++) {
			
			if(i%10==0) {
				System.out.println("The number is divisible by 10:"+i);
			}
			
		}
		

	}

}
